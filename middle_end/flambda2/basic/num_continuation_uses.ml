type t =
  | Zero
  | One
  | Many

let (+) t1 t2 =
  match t1, t2 with
  | Zero, Zero -> Zero
  | Zero, One | One, Zero -> One
  | One, One -> Many
  | _, Many | Many, _ -> Many

let linear = function
  | Zero | One -> true
  | Many -> false
